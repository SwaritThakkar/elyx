export interface TimelineEvent {
  id: string
  type: "message" | "lab" | "plan" | "device" | "decision"
  date: Date
  title: string
  content: string
  sender?: string
  severity?: "low" | "medium" | "high"
  metadata?: Record<string, any>
}

export interface ChatMessage {
  id: string
  content: string
  sender: string
  timestamp: Date
  type: "text" | "image" | "file" | "system"
  status?: "sent" | "delivered" | "read"
  replyTo?: string
  reactions?: { emoji: string; users: string[] }[]
}

export interface LabResult {
  id: string
  date: Date
  testName: string
  value: number
  unit: string
  normalRange: string
  status: "normal" | "high" | "low"
}

export interface DeviceData {
  id: string
  date: Date
  device: "garmin" | "whoop" | "oura" | "dexcom"
  metric: string
  value: number
  unit: string
}

export interface DecisionNode {
  id: string
  // tree nodes in sample data are somewhat flexible, make fields optional
  type?: "condition" | "action" | "evidence" | "outcome"
  title?: string
  description?: string
  confidence?: number
  evidence?: string[]
  options?: string[]
  selected?: string
  children?: DecisionNode[]
  expanded?: boolean
}

